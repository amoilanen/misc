//! AES transformations implementation
//! Based on FIPS 197 Section 5.1

use crate::aes::Block;
use crate::aes::tables::{SBOX, INV_SBOX, GF_MUL_2, GF_MUL_3};

/// SubBytes transformation
/// Replaces each byte in the state with its corresponding value from the S-box
pub fn sub_bytes(block: &mut Block) {
    for i in 0..4 {
        for j in 0..4 {
            block.state[i][j] = SBOX[block.state[i][j] as usize];
        }
    }
}

/// Inverse SubBytes transformation
/// Replaces each byte in the state with its corresponding value from the inverse S-box
pub fn inv_sub_bytes(block: &mut Block) {
    for i in 0..4 {
        for j in 0..4 {
            block.state[i][j] = INV_SBOX[block.state[i][j] as usize];
        }
    }
}

/// ShiftRows transformation
/// Shifts each row of the state matrix by a different offset
pub fn shift_rows(block: &mut Block) {
    // Row 1: Shift left by 1
    let temp = block.state[1][0];
    block.state[1][0] = block.state[1][1];
    block.state[1][1] = block.state[1][2];
    block.state[1][2] = block.state[1][3];
    block.state[1][3] = temp;

    // Row 2: Shift left by 2
    let temp1 = block.state[2][0];
    let temp2 = block.state[2][1];
    block.state[2][0] = block.state[2][2];
    block.state[2][1] = block.state[2][3];
    block.state[2][2] = temp1;
    block.state[2][3] = temp2;

    // Row 3: Shift left by 3
    let temp = block.state[3][3];
    block.state[3][3] = block.state[3][2];
    block.state[3][2] = block.state[3][1];
    block.state[3][1] = block.state[3][0];
    block.state[3][0] = temp;
}

/// Inverse ShiftRows transformation
/// Shifts each row of the state matrix by a different offset in the opposite direction
pub fn inv_shift_rows(block: &mut Block) {
    // Row 1: Shift right by 1
    let temp = block.state[1][3];
    block.state[1][3] = block.state[1][2];
    block.state[1][2] = block.state[1][1];
    block.state[1][1] = block.state[1][0];
    block.state[1][0] = temp;

    // Row 2: Shift right by 2
    let temp1 = block.state[2][2];
    let temp2 = block.state[2][3];
    block.state[2][2] = block.state[2][0];
    block.state[2][3] = block.state[2][1];
    block.state[2][0] = temp1;
    block.state[2][1] = temp2;

    // Row 3: Shift right by 3
    let temp = block.state[3][0];
    block.state[3][0] = block.state[3][1];
    block.state[3][1] = block.state[3][2];
    block.state[3][2] = block.state[3][3];
    block.state[3][3] = temp;
}

/// MixColumns transformation
/// Mixes the columns of the state matrix using GF(2^8) arithmetic
pub fn mix_columns(block: &mut Block) {
    for i in 0..4 {
        let s0 = block.state[0][i];
        let s1 = block.state[1][i];
        let s2 = block.state[2][i];
        let s3 = block.state[3][i];

        // Column mixing matrix multiplication
        block.state[0][i] = GF_MUL_2[s0 as usize] ^ GF_MUL_3[s1 as usize] ^ s2 ^ s3;
        block.state[1][i] = s0 ^ GF_MUL_2[s1 as usize] ^ GF_MUL_3[s2 as usize] ^ s3;
        block.state[2][i] = s0 ^ s1 ^ GF_MUL_2[s2 as usize] ^ GF_MUL_3[s3 as usize];
        block.state[3][i] = GF_MUL_3[s0 as usize] ^ s1 ^ s2 ^ GF_MUL_2[s3 as usize];
    }
}

/// Inverse MixColumns transformation
/// Mixes the columns of the state matrix using GF(2^8) arithmetic in reverse
pub fn inv_mix_columns(block: &mut Block) {
    for i in 0..4 {
        let s0 = block.state[0][i];
        let s1 = block.state[1][i];
        let s2 = block.state[2][i];
        let s3 = block.state[3][i];

        // Inverse column mixing matrix multiplication
        block.state[0][i] = GF_MUL_14[s0 as usize] ^ GF_MUL_11[s1 as usize] ^ GF_MUL_13[s2 as usize] ^ GF_MUL_9[s3 as usize];
        block.state[1][i] = GF_MUL_9[s0 as usize] ^ GF_MUL_14[s1 as usize] ^ GF_MUL_11[s2 as usize] ^ GF_MUL_13[s3 as usize];
        block.state[2][i] = GF_MUL_13[s0 as usize] ^ GF_MUL_9[s1 as usize] ^ GF_MUL_14[s2 as usize] ^ GF_MUL_11[s3 as usize];
        block.state[3][i] = GF_MUL_11[s0 as usize] ^ GF_MUL_13[s1 as usize] ^ GF_MUL_9[s2 as usize] ^ GF_MUL_14[s3 as usize];
    }
}

/// AddRoundKey transformation
/// XORs the state with the round key
pub fn add_round_key(block: &mut Block, round_key: &Block) {
    for i in 0..4 {
        for j in 0..4 {
            block.state[i][j] ^= round_key.state[i][j];
        }
    }
    // For AES-192 and AES-256, XOR the extra words
    if !round_key.extra_words.is_empty() {
        for (i, &byte) in round_key.extra_words.iter().enumerate() {
            if i < block.extra_words.len() {
                block.extra_words[i] ^= byte;
            }
        }
    }
}

